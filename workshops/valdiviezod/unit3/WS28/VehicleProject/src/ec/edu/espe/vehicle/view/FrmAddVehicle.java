public void frmaddVehicle() throws IOException{
        String id;
        String brand;
        String model;     
        id=txtAddId.getText();
        brand = (String) cmbBrand.getSelectedItem();
        model = (String) txtAddModel.getText();
        
        
        Vehicle vehicle = new Vehicle(id, brand, model);
        ArrayList<Vehicle> vehicles = new ArrayList<>();
        Vehicle vehiclesArray[] = new Vehicle[10];
        
        vehicles.add(vehicle);
        vehiclesArray[0]=vehicle;
         
        GsonBuilder gsonBuilder = new GsonBuilder();
        Gson gson = gsonBuilder.create();
        String jsonVehicle;
        jsonVehicle = gson.toJson(vehicle);
        
        
        
        gson = new GsonBuilder().setPrettyPrinting().create();
             try (Writer writer = new FileWriter("Vehicles.json")) {
                 writer.write(gson.toJson(vehicles));
             }
             
        
        Document dc = new Document();
        Connection conection = new Connection();
        dc.append("id",id).append("brand",brand).append("model", model);
        conection.getCollection().insertOne(dc);
        JOptionPane.showMessageDialog(rootPane, "Data has been added successfully check the database");
        
       }
