    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        Product product;
        String productName = txtProductName.getText();
        String Price = txtPrice.getText();
        String Valoration = txtValoration.getText();
        String serialNumber = txtSerialNumber.getText();
        String Description = txaDescription.getText();
        Date sellDate = jdcSellDate.getDate();
        int datetoday = 31;
        
        
        product = new Product(productName, Price, Valoration, serialNumber, Description, sellDate, datetoday);
        JOptionPane.showMessageDialog(null, "Datos almacenados correctamente");
    }//GEN-LAST:event_btnAddActionPerformed

    private void txtProductNameFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtProductNameFocusLost
        String productName = txtProductName.getText();
        boolean isCharacter;
        isCharacter = InputValidation.validateCharacters(productName);
        if(!(isCharacter)){ 
            JOptionPane.showMessageDialog(rootPane, "Enter only characters here");
            txtProductName.requestFocus();
    }//GEN-LAST:event_txtProductNameFocusLost
}

    private void txtPriceFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtPriceFocusLost
        String Price = txtPrice.getText();
        boolean isNumber;
        isNumber = InputValidation.validateCharacters(Price);
        if(!(isNumber)){
            JOptionPane.showMessageDialog(rootPane, "Enter only numbers here");
            txtPrice.requestFocus();
    }//GEN-LAST:event_txtPriceFocusLost
}
