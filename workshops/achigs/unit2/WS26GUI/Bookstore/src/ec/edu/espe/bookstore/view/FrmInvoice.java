    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        Invoice invoice;
        InvoiceController invoiceController;
        String firstName = txtFirstName.getText();
        String lastName = txtLastName.getText();
        String password = pwdPassword.getText();
        String phoneNumber = txtPhoneNumber.getText();
        String email = txtEmail.getText();
        String Descrption = txaDescription.getText();
        String typeDocument = cmbTypeDocument.getSelectedItem().toString();
        ArrayList<Book> books = new ArrayList<>();
        LocalDateTime birthDate = LocalDateTime.now();
        int amount = 9;
        
        invoice = new Invoice(firstName, lastName, password, phoneNumber, email, email, Descrption, typeDocument, books, birthDate, amount);
        invoiceController = new InvoiceController(invoice, this, books);
        
        invoiceController.addData(invoice);
        if(firstName.isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "missing first name, please enter your first name to save the contact");
        }
    }//GEN-LAST:event_btnAddActionPerformed

