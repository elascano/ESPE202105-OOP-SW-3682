    private void btnSaveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSaveMouseClicked
        String name = txtName.getText();
        String lastName = txtLastName.getText();
        String id = txtIdentification.getText();
        String userName = txtUserName.getText();
        String password = txtPassword.getText();

        if(name.isEmpty() || lastName.isEmpty() || id.isEmpty() || userName.isEmpty() || password.isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Debe LLenar todos los Campos");
        }else{
            document.put("id", txtIdentification.getText());
            document.put("name", txtName.getText());
            document.put("lastName", txtLastName.getText());
            document.put("userName", txtUserName.getText());
            document.put("password", txtPassword.getText());
            connection = new DBManager("users");
            connection.insert(document);
            FrmBookstore bookstore = new FrmBookstore();
            bookstore.setVisible(true);
            dispose();
        }
    }//GEN-LAST:event_btnSaveMouseClicked

