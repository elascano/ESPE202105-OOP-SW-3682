    private void validationCharacterOnly( JTextField jTextField) throws HeadlessException {
        String brand = jTextField.getText();
        boolean isCharacter;
        isCharacter = InputValidation.validateCharacters(brand);
        if(!(isCharacter)){
            JOptionPane.showMessageDialog(rootPane, "Enter only characters here");
            jTextField.requestFocus();
        }
    }

