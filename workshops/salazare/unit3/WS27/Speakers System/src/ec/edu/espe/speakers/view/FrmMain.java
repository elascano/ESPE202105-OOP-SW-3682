    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
         JOptionPane.showMessageDialog(rootPane, "Reading the data...");
        try (MongoClient mongoClient = MongoClients.create(System.getProperty("mongodb.uri"))) {
            MongoDatabase sampleTrainingDB = mongoClient.getDatabase("Speaker");
            MongoCollection<Document> speakersCollection = sampleTrainingDB.getCollection("speakers");
            // find one document with new Document
            Document models = speakersCollection.find(new Document("Models", 100)).first();
            System.out.println("Models 1: " + models.toJson());
            // find one document with Filters.eq()
            Document manofacturer = speakersCollection.find(eq("Models", 100)).first();
            System.out.println("Models 2: " + manofacturer.toJson());
            // find a list of documents and iterate throw it using an iterator.
            FindIterable<Document> iterable = speakersCollection.find(gte("Models", 100));
            MongoCursor<Document> cursor = iterable.iterator();
            while (cursor.hasNext()) {
                System.out.println(cursor.next().toJson());
            }
            // find a list of documents and use a List object instead of an iterator
            List<Document> speakersList = speakersCollection.find(gte("models", 100)).into(new ArrayList<>());
            System.out.println("Student list with an ArrayList:");
            for (Document student : speakersList) {
                System.out.println(student.toJson());
            }
         }
    }//GEN-LAST:event_jButton2ActionPerformed

