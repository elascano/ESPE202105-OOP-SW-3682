    private void btnAddProductActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddProductActionPerformed
        Product product;
        ProductController productController;
        String brand = txtBrand.getText();
        String model = txtModel.getText();
        String password = pwdPassword.getText();
        String price = txtPrice.getText();
        String productID = txtProductID.getText();
        String description = txaDescription.getText();
        String color = cmbColor.getSelectedItem().toString();
        ArrayList <Category> categorys = new ArrayList<>();
        LocalDateTime dateProductAggregation = LocalDateTime.now();
        int amount = 3;
        
        product = new Product(brand, model, password, price, description, color, price, dateProductAggregation, amount);
        productController = new ProductController(product, categorys, this);
        
        productController.addProduct(product);
        if (brand.isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Missing Brand, pls enter the Brand to save product");
        }
    }//GEN-LAST:event_btnAddProductActionPerformed

