package ec.edu.espe.basicoperation.utils;

/**
 *
 * @author 59399
 */
public class Operation {
    public static float sum(float firstAddend, float secondAddend){
        
        float total; 
        total = firstAddend + secondAddend;
        return total;
    }
    
    public static float substract(float minuend, float substracted){
     
        float difference;
        difference = minuend - substracted;
        return difference;
    }
    
}
