    private void btnAddUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddUserActionPerformed
    
        User user;
        UserController userController;
        
        String firstName = txtFirstName.getText();
        String lastName = txtLastName.getText();
        String shopUser = txtShopUser.getText();
        String password = pwPassword.getText();
        String phoneNumber = txtPhoneNumber.getText();
        String email = txtEmail.getText();
        String address = txtAddress.getText();
        String comments = txaComments.getText();
        String gender = cmbGender.getSelectedItem().toString();
        ArrayList<Interest> hobbies = new ArrayList<>();
        LocalDateTime birthDate = LocalDateTime.now();
        int workHours = 0;
        
        user = new User(firstName, lastName, phoneNumber, email, address, gender, shopUser, password, hobbies, birthDate, comments, workHours);
        userController = new UserController(user, this, hobbies);
        
        userController.addUser(user);
        
        if (firstName.isEmpty()){
        JOptionPane.showMessageDialog(this, "Enter the First Name");
        }
    }//GEN-LAST:event_btnAddUserActionPerformed

    private void txtFirstNameFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtFirstNameFocusLost
        String firstName = txtFirstName.getText();
        boolean isCharacter;
        isCharacter = inputValidation.validateCharacters(firstName);
        if (isCharacter){
        JOptionPane.showMessageDialog(this, "Enter only caracter here");
        txtFirstName.requestFocus();
        }
    }//GEN-LAST:event_txtFirstNameFocusLost

