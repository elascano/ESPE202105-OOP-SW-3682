package ec.edu.espe.question22.model;

/**
 *
 * @author 59399
 */
public class J {
    
    private float z;

    public J(float z) {
        this.z = z;
    }

    @Override
    public String toString() {
        return "J{" + "z=" + z + '}';
    }

    
    
    /**
     * @return the z
     */
    public float getZ() {
        return z;
    }

    /**
     * @param z the z to set
     */
    public void setZ(float z) {
        this.z = z;
    }
    
    
    
}
