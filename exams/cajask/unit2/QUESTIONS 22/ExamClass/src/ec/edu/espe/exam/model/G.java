package ec.edu.espe.exam.model;

/**
 *
 * @author Lizbeth Cajas
 */
public class G {
    private int H;
    private int J;

    public G(int H, int J) {
        this.H = H;
        this.J = J;
    }

    
    
    /**
     * @return the H
     */
    public int getH() {
        return H;
    }

    /**
     * @param H the H to set
     */
    public void setH(int H) {
        this.H = H;
    }

    /**
     * @return the J
     */
    public int getJ() {
        return J;
    }

    /**
     * @param J the J to set
     */
    public void setJ(int J) {
        this.J = J;
    }
    
    
}
