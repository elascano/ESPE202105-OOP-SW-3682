package ec.edu.espe.exam.model;

/**
 *
 * @author Lizbeth Cajas
 */
public class E {
    private int  C;
    private int D;

    public E(int C, int D) {
        this.C = C;
        this.D = D;
    }

    
    /**
     * @return the C
     */
    public int getC() {
        return C;
    }

    /**
     * @param C the C to set
     */
    public void setC(int C) {
        this.C = C;
    }

    /**
     * @return the D
     */
    public int getD() {
        return D;
    }

    /**
     * @param D the D to set
     */
    public void setD(int D) {
        this.D = D;
    }
    
    
}
