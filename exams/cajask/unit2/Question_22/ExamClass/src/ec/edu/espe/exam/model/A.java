public class A {
    private int B;
    private int D;
    private int A;

    public A(int A) {
        this.A = A;
    }

    public A(int B, int D) {
        this.B = B;
        this.D = D;
    }

    
    
    
    /**
     * @return the B
     */
    public int getB() {
        return B;
    }

    /**
     * @param B the B to set
     */
    public void setB(int B) {
        this.B = B;
    }

    /**
     * @return the D
     */
    public int getD() {
        return D;
    }

    /**
     * @param D the D to set
     */
    public void setD(int D) {
        this.D = D;
    }

    /**
     * @return the A
     */
    public int getA() {
        return A;
    }

    /**
     * @param A the A to set
     */
    public void setA(int A) {
        this.A = A;
    }
    
}
