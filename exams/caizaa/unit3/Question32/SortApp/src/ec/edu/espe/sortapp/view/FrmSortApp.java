    private void btnOrderMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnOrderMouseClicked
        int size;
        String sortAlg;
        int sortedList[] = {};
        String unsorted = " ";
                for (int i = 0; i < data.size(); i++) {
                    unsorted += data.get(i) + "\n";
                }
                
        int data[] = {};
        
        for(int i=0; i<data.length; i++){
            data[i]=Integer.parseInt(txtNumberForOrders.getText());
        }
        
        sortAlg = sortContext(data);
        
        SortingContext sc = new SortingContext();
        sortedList = sc.sort(data);
        
        
        size = data.length;
        
        Document sort = new Document("_id", new ObjectId());
        sort.append("unsorted", unsorted)
            .append("size", size)
            .append("sort algorithm", sortAlg)
            .append("sorted", sortedList);
            
        collection.insertOne(sort);
        
    }//GEN-LAST:event_btnOrderMouseClicked

    private void btnAddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAddMouseClicked
            
        if(!validate(txtNumberForOrders.getText().trim())){    
            JOptionPane.showMessageDialog(null, "Insert Only numbers   ");
            txtNumberForOrders.setText(" ");
        } else {
            if (!txtNumberForOrders.getText().equals("")){
                data.add(Integer.parseInt(txtNumberForOrders.getText().toString()));
                txtNumberForOrders.setText("");
                txtNumberForOrders.requestFocus();
                txaNumbers.setText("");
                String list = "";
                for (int i = 0; i < data.size(); i++) {
                    list += data.get(i) + "\n";
                }
                txaNumbers.setText(list);
            } else {
                JOptionPane.showMessageDialog(null, "Insert Data to the List");
            }
        }
    }//GEN-LAST:event_btnAddMouseClicked

    public String sortContext(int data[]){
        String sortAlg = "";
        
        if(data.length > 0 && data.length < 4){
            sortAlg = "Bubble sort";
        } 
        if (data.length >= 4 && data.length < 8){
            sortAlg = "Insertion Sort";
        }
        if (data.length >= 8){
            sortAlg = "Quick Sort";
        }
        
        return sortAlg;
    }
    
    public static boolean validate(String datas){
        return datas.matches("[0-9]*");
    }
    
