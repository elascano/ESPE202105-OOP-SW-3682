    private void calculator(){
        float value;
        float totalTax;
        USTax usTax = USTax.getInstace();
        
        value = Float.parseFloat(txtAmount.getText());
        
        totalTax=usTax.salesTotal(value);
        
        txtTotal.setText(String.valueOf(totalTax));
    }
    
    
    
    
    
    private void calc(){
        float value;
        float iva;
        float totalTax;
        
        
        value = Float.parseFloat(txtAmount.getText());
        iva = CalculateTax.computeIva(value);
        totalTax = CalculateTax.computeTotalPrice(value);
        
        txtTotal.setText(String.valueOf(totalTax));
    }
