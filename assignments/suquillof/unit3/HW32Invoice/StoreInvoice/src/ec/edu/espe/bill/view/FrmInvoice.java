    private void setData() {
           
        jlbName.setText(FrmClient.txtName.getText());
        jlbIdNumber.setText(FrmClient.txtIdNumber.getText());
        jlbAddress.setText(FrmClient.txtAddress.getText());
        jlbPhoneNumber.setText(FrmClient.txtPhoneNumber.getText());
        jlbProduct.setText(FrmClient.txtProduct.getText());
        jlbPrice.setText(FrmClient.txtPrice.getText());     
        setTax();
        setTotal();
    }

    private void setTax() {

        String percentage;

        USTax usTax = USTax.getInstance();
        percentage = String.valueOf(usTax.getTaxPercentage());

        jlbTax.setText(percentage);

    }

    private void setTotal() {

        float price = Float.parseFloat(FrmClient.txtPrice.getText());
        float total;
        String totalPrice;
        
        USTax usTax = USTax.getInstance();
        
        total = usTax.computeSalesTotal(price);
        totalPrice = String.valueOf(total);
        
        jlbTotal.setText(totalPrice);
    }

