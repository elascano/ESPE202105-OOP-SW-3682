package ec.edu.espe.cliente.model;

/**
 *
* @author Lizbeth Cajas
 */
public class Client {
    private String id;
    private String name;
    private int age;

    public Client() {
    }

    public Client(String id, String name, int age) {
        this.id = id;
        this.name = name;
        this.age = age;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    @Override
    public String toString() {
        return "Client{" + "id=" + id + ", name=" + name + ", age=" + age + '}';
    }

}
