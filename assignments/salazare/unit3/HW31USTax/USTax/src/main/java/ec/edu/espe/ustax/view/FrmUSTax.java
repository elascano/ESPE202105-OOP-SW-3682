    private void computeSalesTotal() {
        float value;
        float salesTotal;
        USTax usTax = USTax.getInstance();
        
        value = Float.parseFloat(txtAmount.getText());
        salesTotal = usTax.computeSalesTotal(value);
        
        txtTotalPrice.setText(String.valueOf(salesTotal));
    }
