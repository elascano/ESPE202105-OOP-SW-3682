     private void computeSalesTotal(){
        float value;
        float salesTotal;
        float product1;
        float product2;
        float product3;
       USTax usTax = USTax.getInstance();
        
        
        value = Float.parseFloat(txtProduct3.getText());
        product1 = Float.parseFloat(txtProduct1.getText());
        product2 = Float.parseFloat(txtProduct2.getText());
        product3 = Float.parseFloat(txtProduct3.getText());
        salesTotal = usTax.computeTotal(value+product1+product2);
        
        txtAmount.setText(String.valueOf(product1+product2+product3));
        txtTotal.setText(String.valueOf(salesTotal));
    }
