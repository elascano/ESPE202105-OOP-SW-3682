    private void btnChargerAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChargerAddActionPerformed
        String brand = txtBrand.getText();
        String model = txtChargerModel.getText();
        String color = cmbColor.getSelectedItem().toString();
        float price = Float.parseFloat(txtPrice.getText());
        int id = Integer.valueOf(txtChargerId.getText());
        int stock = (int) spnStock.getValue();
        String entryType = cmbEntryType.getSelectedItem().toString();
        
        Charger.addCharger(color, model, id, price, brand, entryType, stock);
    }//GEN-LAST:event_btnChargerAddActionPerformed

    private void btnChargerDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChargerDeleteActionPerformed
        int id = Integer.valueOf(txtChargerId.getText());
        MongoClient conection = createConection();
        DB db = conection.getDB("Products");
        DBCollection colection = db.getCollection("Charger");
        colection.remove(new BasicDBObject().append("Id", id));
    }//GEN-LAST:event_btnChargerDeleteActionPerformed

