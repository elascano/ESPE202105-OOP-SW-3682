    private void btnAddCellphoneActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddCellphoneActionPerformed
        
        String brand = cmbCellphoneBrand.getSelectedItem().toString();
        String model = txtCellPhoneModel.getText();
        String color = cmbCellphoneColor.getSelectedItem().toString();
        float price = Float.parseFloat(txtCellphonePrice.getText());
        int id = Integer.valueOf(txtCellphoneId.getText());
        int stock = (int) spnStock.getValue();
        
        CellPhone.addCellphone(color, model, id, price, brand, stock);
    }//GEN-LAST:event_btnAddCellphoneActionPerformed

    private void btnDeleteCellphoneActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteCellphoneActionPerformed
        int id = Integer.valueOf(txtCellphoneId.getText());
        MongoClient conection = createConection();
        DB db = conection.getDB("Products");
        DBCollection collection = db.getCollection("Cellphone");
        collection.remove(new BasicDBObject().append("Id", id));
    }//GEN-LAST:event_btnDeleteCellphoneActionPerformed

