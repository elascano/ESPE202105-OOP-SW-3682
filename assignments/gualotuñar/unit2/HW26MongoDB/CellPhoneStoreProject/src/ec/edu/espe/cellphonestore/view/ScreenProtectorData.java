    private void btnScreenProtectorAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnScreenProtectorAddActionPerformed
        String brand = txtScreenProtectorBrand.getText();
        String model = txtScreenProtectorModel.getText();
        float price = Float.parseFloat(txtScreenProtectorPrice.getText());
        int id = Integer.valueOf(txtScreenProtectorId.getText());
        int stock = (int) spnStock.getValue();
        
        ScreenProtector.addScreenProtector(model, model, id, price, brand, stock);
    }//GEN-LAST:event_btnScreenProtectorAddActionPerformed

    private void btnScreenProtectorDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnScreenProtectorDeleteActionPerformed
        int id = Integer.valueOf(txtScreenProtectorId.getText());
        MongoClient conection = createConection();
        DB db = conection.getDB("Products");
        DBCollection colection = db.getCollection("ScreenProtector");
        colection.remove(new BasicDBObject().append("Id", id));
    }//GEN-LAST:event_btnScreenProtectorDeleteActionPerformed

