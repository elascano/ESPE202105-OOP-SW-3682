    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        String brand = txtMarca.getText();
        String model = txtModelo.getText();
        String color = cmbColor.getSelectedItem().toString();
        float price = Float.parseFloat(txtPrecio.getText());
        int id = Integer.valueOf(txtId.getText());
        int stock = (int) spnStock.getValue();
        
        HeadPhone.addHeadphone(color, model, id, price, brand, stock);
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int id = Integer.valueOf(txtId.getText());
        MongoClient conection = createConection();
        DB db = conection.getDB("Products");
        DBCollection colection = db.getCollection("Headphone");
        colection.remove(new BasicDBObject().append("Id", id));
    }//GEN-LAST:event_btnEliminarActionPerformed

