    private void btnAddPhoneCaseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddPhoneCaseActionPerformed
        String brand = txtCellphoneCaseBrand.getText();
        String model = txtPhoneCaseModel.getText();
        String color = cmbPhoneCaseColor.getSelectedItem().toString();
        float price = Float.parseFloat(txtPrice.getText());
        int id = Integer.valueOf(txtPhoneCaseId.getText());
        int stock = (int) spnStock.getValue();
        
        PhoneCase.addPhoneCase(color, model, id, price, brand, stock);
    }//GEN-LAST:event_btnAddPhoneCaseActionPerformed

    private void btnDeletePhoneCaseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeletePhoneCaseActionPerformed
        int id = Integer.valueOf(txtPhoneCaseId.getText());
        MongoClient conection = createConection();
        DB db = conection.getDB("Products");
        DBCollection colection = db.getCollection("PhoneCase");
        colection.remove(new BasicDBObject().append("Id", id));
    }//GEN-LAST:event_btnDeletePhoneCaseActionPerformed

