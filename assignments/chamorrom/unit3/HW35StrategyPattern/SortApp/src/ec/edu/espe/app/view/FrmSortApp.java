    public void Sort(){
    int data[] = null;
    String string= txtData.getText();
    
    //string = Integer.parseInt(data);
    SortingContext sc = new SortingContext();
    int sortedList[] = sc.sort(data);
    
    for (int i=0;i<data.length;i++){
       Integer box;
       box = Integer.valueOf(txtData.getText());
       if(box>0 && box<5)
           lbSorting.setText("BubbleSort");
            txaSort.setText(String.valueOf(data[i]));
       if(box>=5 && box<10)
           lbSorting.setText("InsertionSort");
            txaSort.setText(String.valueOf(data[i]));
       if(box>=10)
           lbSorting.setText("QuickSort");
            txaSort.setText(String.valueOf(data[i]));
    }
    }    
   
    
